##Make a Program that asks for the temperature in degrees Celsius, transform and display it in degrees Fahrenheit.
## Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Fahrenheit.


c=float(input("Digite a temperatura em graus Celsius para converter em Fahrenheit : "))


a=((9*c)/5)+32
print(" A temperatura em Fahrenheit é : " + str(a)+"ºF")

