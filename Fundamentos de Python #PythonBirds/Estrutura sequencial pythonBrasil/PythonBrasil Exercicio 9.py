## Make a Program that asks for the temperature in degrees Fahrenheit, transform and display the temperature in degrees Celsius. C = 5 * ((F-32) / 9).
a=float(input("Digite a temperatura em graus Fahrenheit para converter em Celsius : "))

c=5*((a-32)/9)
print(" A temperatura em Celsius é : " + str(c)+"ºC")
